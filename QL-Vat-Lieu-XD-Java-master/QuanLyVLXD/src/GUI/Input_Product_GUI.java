    public void GetCate(){
        Categories = CategoryDLL.GetAllCate();
        int i=0;
        String[] list = new String[Categories.size()];
        for(Category cate : Categories){
            list[i] = cate.getTenLoai();
            i++;
        }
        DefaultComboBoxModel<String> aModel =  new DefaultComboBoxModel<>(list);
        cbCategory.setModel(aModel);
    }

