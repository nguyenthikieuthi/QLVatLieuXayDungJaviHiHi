    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String maloai = Categories.get(cbCategory.getSelectedIndex()).getMaloai();
        Products prod = new Products(txtMaSP.getText(),txtTenSp.getText(),txtNhaSanXuat.getText(),maloai);
        if(ProductFormMain == null)
        {
            SanPhamDLL.AddProduct(prod);
        }
        else
        
            SanPhamDLL.UpdateProduct(prod);
                
    }//GEN-LAST:event_jButton1ActionPerformed

